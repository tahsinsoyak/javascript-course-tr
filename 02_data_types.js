/* VAR */
//veri tanımlamak için var => variable [genelde string]
//daha sonra veri tipi değişebilir.
//bütün program boyunca kullanılır.

var myName = "Tahsin";
myName = 8;

/* LET */
//sadece tanımladığımız aralıkta kullanılır.
let ourName = "Suntheo";

/* CONST */
//sadece bir kez tanımlanır, değiştirilmez.
